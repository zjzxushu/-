#pragma once


#include <mysql.h>
#include <string>
using namespace std;
#include "public.h"
#include<ctime>
// 数据库操作类
class Connection
{
public:
	// 初始化数据库连接
	Connection();
	
	// 释放数据库连接资源
	~Connection();
	
	// 连接数据库
	bool connect(string ip, 
			unsigned short port,
			string user, 
			string password,
			string dbname);

	// 更新操作 insert、delete、update
	bool update(string sql);
	
	// 查询操作 select
	MYSQL_RES* query(string sql);

	//刷新当前空闲连接的存活时间
	void refreshAliveTime() { _allivetime = clock(); }
	//返回当前空闲连接已经存活了的时间
	clock_t getAliveTime() const{ return clock() - _allivetime; }
private:
	MYSQL* _conn; // 表示和MySQL Server的一条连接
	clock_t _allivetime;//记录进入空闲状态连接的初始存活时间
};